    def get_page(self) -> List[dict]:
        historys = self.get_data('sign_history') or []
        if not historys:
            return [
                {
                    'component': 'VAlert',
                    'props': {
                        'type': 'info',
                        'variant': 'tonal',
                        'text': '暂无签到记录，请先配置 Cookie 并启用插件',
                        'class': 'mb-2'
                    }
                }
            ]

        historys = sorted(historys, key=lambda x: x.get("date", ""), reverse=True)
        rows = []
        for history in historys:
            status_text = history.get("status", "未知")
            status_color = "success" if ("成功" in status_text or "已签到" in status_text) else "error"
            reward_text = "-"
            if history.get("gain") is not None:
                reward_text = f"{history.get('gain')} 鸡腿"
            rows.append({
                'component': 'tr',
                'content': [
                    {'component': 'td', 'text': history.get("date", "")},
                    {
                        'component': 'td',
                        'content': [
                            {
                                'component': 'VChip',
                                'props': {'color': status_color, 'size': 'small', 'variant': 'outlined'},
                                'text': status_text
                            }
                        ]
                    },
                    {'component': 'td', 'text': reward_text}
                ]
            })

        return [
            {
                'component': 'VCard',
                'props': {'variant': 'outlined', 'class': 'mb-4'},
                'content': [
                    {'component': 'VCardTitle', 'props': {'class': 'text-h6'}, 'text': 'DeepFlood 签到历史'},
                    {
                        'component': 'VCardText',
                        'content': [
                            {
                                'component': 'VTable',
                                'props': {'hover': True, 'density': 'compact'},
                                'content': [
                                    {
                                        'component': 'thead',
                                        'content': [
                                            {
                                                'component': 'tr',
                                                'content': [
                                                    {'component': 'th', 'text': '时间'},
                                                    {'component': 'th', 'text': '状态'},
                                                    {'component': 'th', 'text': '奖励'}
                                                ]
                                            }
                                        ]
                                    },
                                    {'component': 'tbody', 'content': rows}
                                ]
                            }
                        ]
                    }
                ]
            }
        ]

