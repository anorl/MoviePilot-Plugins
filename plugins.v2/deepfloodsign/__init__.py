    def _get_cookiecloud_crypt_key(self) -> bytes:
        md5_generator = md5()
        md5_generator.update(
            (str(settings.COOKIECLOUD_KEY).strip() + '-' + str(settings.COOKIECLOUD_PASSWORD).strip()).encode('utf-8'))
        return (md5_generator.hexdigest()[:16]).encode('utf-8')

    def _match_cookiecloud_domain(self, cookie_data: Dict[str, Any], domain: str) -> Tuple[Optional[str], List[Dict[str, Any]]]:
        if not isinstance(cookie_data, dict) or not domain:
            return None, []

        normalized_domain = domain.strip().lower()
        target_domains = {normalized_domain, f'.{normalized_domain}'}
        if normalized_domain.startswith('www.'):
            bare = normalized_domain[4:]
            target_domains.update({bare, f'.{bare}'})
        else:
            target_domains.update({f'www.{normalized_domain}', f'.www.{normalized_domain}'})

        aggregated = []
        matched_keys = []

        for key, value in cookie_data.items():
            if not isinstance(value, list) or not value:
                continue

            key_norm = str(key).strip().lower()
            key_hit = key_norm in target_domains or key_norm.endswith(f'.{normalized_domain}') or normalized_domain.endswith(key_norm.lstrip('.'))

            for item in value:
                if not isinstance(item, dict):
                    continue
                item_domain = str(item.get('domain') or key_norm or '').strip().lower()
                if item_domain.startswith('#'):
                    continue
                domain_hit = item_domain in target_domains or item_domain.endswith(f'.{normalized_domain}') or normalized_domain.endswith(item_domain.lstrip('.'))
                if key_hit or domain_hit:
                    aggregated.append(item)
                    if key not in matched_keys:
                        matched_keys.append(key)

        if aggregated:
            deduped = []
            seen = set()
            for item in aggregated:
                name = str(item.get('name') or '').strip()
                value = str(item.get('value') or '').strip()
                item_domain = str(item.get('domain') or '').strip().lower()
                key = (name, item_domain)
                if not name or not value or key in seen:
                    continue
                seen.add(key)
                deduped.append(item)
            return ','.join(matched_keys) if matched_keys else normalized_domain, deduped

        return None, []

    def _build_cookie_header(self, cookie_items: List[Dict[str, Any]]) -> str:
        pairs = []
        for item in cookie_items or []:
            if not isinstance(item, dict):
                continue
            name = str(item.get("name") or "").strip()
            value = str(item.get("value") or "").strip()
            if name and value:
                pairs.append(f"{name}={value}")
        return "; ".join(pairs)

    def _decrypt_cookiecloud_file(self) -> Tuple[Dict[str, Any], str]:
        if not getattr(settings, 'COOKIECLOUD_ENABLE_LOCAL', False):
            return {}, "MoviePilot 未启用本地 CookieCloud"
        if not getattr(settings, 'COOKIECLOUD_KEY', None):
            return {}, "MoviePilot 未配置 COOKIECLOUD_KEY"
        if not getattr(settings, 'COOKIECLOUD_PASSWORD', None):
            return {}, "MoviePilot 未配置 COOKIECLOUD_PASSWORD"

        cookie_path = settings.COOKIE_PATH / f"{settings.COOKIECLOUD_KEY}.json"
        logger.info(f"[deepfloodsign] 准备读取本地 CookieCloud 文件: {cookie_path}")
        if not cookie_path.exists():
            return {}, f"本地 CookieCloud 文件不存在: {cookie_path}"

        try:
            raw = json.loads(cookie_path.read_text(encoding='utf-8'))
        except Exception as e:
            return {}, f"读取 CookieCloud 文件失败: {str(e)}"

        encrypted = raw.get("encrypted")
        if not encrypted:
            return {}, "CookieCloud 文件缺少 encrypted 字段"

        try:
            decrypted_data = CryptoJsUtils.decrypt(encrypted, self._get_cookiecloud_crypt_key()).decode('utf-8')
            payload = json.loads(decrypted_data)
        except Exception as e:
            return {}, f"解密 CookieCloud 文件失败: {str(e)}"

        cookie_data = payload.get("cookie_data") if isinstance(payload, dict) else None
        if isinstance(cookie_data, dict):
            return cookie_data, ""
        if isinstance(payload, dict):
            return payload, ""
        return {}, "CookieCloud 解密结果为空或格式不正确"

    def _decrypt_cookiecloud_payload(self, payload: Dict[str, Any]) -> Tuple[Dict[str, Any], str]:
        if not isinstance(payload, dict):
            return {}, "CookieCloud 响应格式不正确"

        encrypted = payload.get("encrypted")
        if encrypted:
            try:
                decrypted_data = CryptoJsUtils.decrypt(encrypted, self._get_cookiecloud_crypt_key()).decode('utf-8')
                payload = json.loads(decrypted_data)
            except Exception as e:
                return {}, f"解密 CookieCloud 响应失败: {str(e)}"

        cookie_data = payload.get("cookie_data") if isinstance(payload, dict) else None
        if isinstance(cookie_data, dict):
            return cookie_data, ""
        if isinstance(payload, dict):
            return payload, ""
        return {}, "CookieCloud 响应中未找到有效 cookie_data"

    def _fetch_remote_cookiecloud_data(self) -> Tuple[Dict[str, Any], str]:
        host = (getattr(settings, 'COOKIECLOUD_HOST', '') or '').strip().rstrip('/')
        if not host:
            return {}, "MoviePilot 未配置 COOKIECLOUD_HOST"
        if not getattr(settings, 'COOKIECLOUD_KEY', None):
            return {}, "MoviePilot 未配置 COOKIECLOUD_KEY"
        if not getattr(settings, 'COOKIECLOUD_PASSWORD', None):
            return {}, "MoviePilot 未配置 COOKIECLOUD_PASSWORD"

        if requests is None:
            return {}, "requests 未安装，无法请求远端 CookieCloud"

        candidates = [
            f"{host}/get/{settings.COOKIECLOUD_KEY}",
            f"{host}/get/{settings.COOKIECLOUD_KEY}.json",
            f"{host}/{settings.COOKIECLOUD_KEY}",
            f"{host}/{settings.COOKIECLOUD_KEY}.json",
        ]
        logger.info(f"[deepfloodsign] 本地 CookieCloud 未启用，尝试远端 CookieCloud: {host}")

        last_error = ""
        for url in candidates:
            try:
                logger.info(f"[deepfloodsign] 尝试请求远端 CookieCloud: {url}")
                resp = requests.get(url, timeout=20, verify=self._verify_ssl)
                if getattr(resp, 'status_code', None) != 200:
                    last_error = f"HTTP {getattr(resp, 'status_code', None)}"
                    logger.warning(f"[deepfloodsign] 远端 CookieCloud 请求失败: {url}, {last_error}")
                    continue
                try:
                    payload = resp.json()
                except Exception as e:
                    last_error = f"响应非 JSON: {str(e)}"
                    logger.warning(f"[deepfloodsign] 远端 CookieCloud 响应解析失败: {url}, {last_error}")
                    continue

                cookie_data, error = self._decrypt_cookiecloud_payload(payload)
                if error:
                    last_error = error
                    logger.warning(f"[deepfloodsign] 远端 CookieCloud 数据处理失败: {url}, {error}")
                    continue
                return cookie_data, ""
            except Exception as e:
                last_error = str(e)
                logger.warning(f"[deepfloodsign] 请求远端 CookieCloud 异常: {url}, {str(e)}")

        return {}, f"远端 CookieCloud 获取失败: {last_error or '未知错误'}"

    def _load_cookie_from_cookiecloud(self) -> Tuple[Optional[str], str]:
        domain = self._resolve_cookiecloud_domain()
        if not domain:
            return None, "无法从 site_url 推导 DeepFlood 域名"

        logger.info(f"[deepfloodsign] CookieCloud 模式启用，目标域名: {domain}")
        if getattr(settings, 'COOKIECLOUD_ENABLE_LOCAL', False):
            cookie_data, error = self._decrypt_cookiecloud_file()
        else:
            cookie_data, error = self._fetch_remote_cookiecloud_data()
        if error:
            return None, error

        matched_domain, cookie_items = self._match_cookiecloud_domain(cookie_data, domain)
        if not matched_domain or not cookie_items:
            return None, f"CookieCloud 中未找到域名 {domain} 的 Cookie"

        cookie_header = self._build_cookie_header(cookie_items)
        if not cookie_header:
            return None, f"CookieCloud 域名 {matched_domain} 下未拼出有效 Cookie"

        logger.info(
            f"[deepfloodsign] 已从 CookieCloud 命中域名 {matched_domain}，Cookie 项数量: {len(cookie_items)}，Cookie摘要: {self._mask_cookie_for_log(cookie_header)}"
        )
        return cookie_header, ""

    def _get_active_cookie(self) -> Tuple[Optional[str], str]:
        source = (self._cookie_source or "manual").strip().lower()
        if source == "cookiecloud":
            cookie, error = self._load_cookie_from_cookiecloud()
            if error:
                logger.error(f"[deepfloodsign] CookieCloud 获取失败: {error}")
                return None, error
            return cookie, ""

        cookie = (self._cookie or "").strip()
        if not cookie:
            return None, "未配置手工 Cookie"
        logger.info(f"[deepfloodsign] 使用手工 Cookie，Cookie摘要: {self._mask_cookie_for_log(cookie)}")
        return cookie, ""

    def _run_api_sign(self) -> Dict[str, Any]:
        """尽量复用 nodeseeksign 的签到逻辑：API优先 + 非JSON兜底 + cloudscraper预热重试"""
        try:
            result = {"success": False, "signed": False, "already_signed": False, "message": "", "gain": None}

            if not self._site_url:
                result["message"] = "未配置站点 URL"
                return result
            active_cookie, cookie_error = self._get_active_cookie()
            if not active_cookie:
                result["message"] = cookie_error or "未获取到可用 Cookie"
                return result

            url = self._build_attendance_url()
            origin = self._site_url
            referer = f"{self._site_url}/board"

            headers = {
                'Accept': '*/*',
                'Accept-Encoding': 'gzip, deflate, br, zstd',
                'Accept-Language': 'zh-CN,zh;q=0.9,en;q=0.8',
                'Content-Length': '0',
                'Content-Type': 'application/json',
                'Origin': origin,
                'Referer': referer,
                'Sec-Fetch-Dest': 'empty',
                'Sec-Fetch-Mode': 'cors',
                'Sec-Fetch-Site': 'same-origin',
                'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/136.0.0.0 Safari/537.36',
                'Cookie': active_cookie,
            }

            proxies = self._get_proxies()

            # 主请求
            if self._attendance_method == 'GET':
                response = self._smart_get(url=url, headers=headers, proxies=proxies, timeout=30)
            else:
                response = self._smart_post(url=url, headers=headers, data=b'', json_body=None, proxies=proxies, timeout=30)

            try:
                logger.info(f"签到响应状态码: {getattr(response, 'status_code', None)}")
                ct = response.headers.get('Content-Type') or response.headers.get('content-type')
                if ct:
                    logger.info(f"签到响应Content-Type: {ct}")
            except Exception:
                pass

            # 优先 JSON
            try:
                data = response.json()
                msg = data.get('message', '') if isinstance(data, dict) else ''

                gain = self._extract_gain(data, msg)
                if isinstance(data, dict) and data.get('success') is True:
                    result.update({"success": True, "signed": True, "message": msg})
                elif msg and ("鸡腿" in msg or ("签到" in msg and ("成功" in msg or "完成" in msg))):
                    result.update({"success": True, "signed": True, "message": msg})
                elif msg and ("已完成签到" in msg or "今日已签到" in msg):
                    result.update({"success": True, "already_signed": True, "message": msg})
                elif msg == "USER NOT FOUND" or (isinstance(data, dict) and data.get('status') == 404):
                    result.update({"message": "Cookie已失效，请更新"})
                else:
                    result.update({"message": msg or f"未知响应: {getattr(response, 'status_code', None)}"})
                if gain is not None:
                    result["gain"] = gain
                return result

            except Exception:
                # 非 JSON：记录片段 + cloudscraper 预热重试（复用 nodeseeksign 思路）
                text = getattr(response, 'text', '') or ''
                snippet = text[:400] if len(text) > 400 else text
                logger.warning(f"非JSON签到响应文本片段: {snippet}")
                try:
                    self.save_data('last_sign_response', {
                        'status_code': getattr(response, 'status_code', None),
                        'content_type': getattr(getattr(response, 'headers', {}), 'get', lambda *_: '')('Content-Type', ''),
                        'text_snippet': snippet
                    })
                except Exception:
                    pass

                try:
                    warm = self._scraper_warmup_and_attach_user_cookie()
                    if warm and self._attendance_method != 'GET':
                        logger.info("尝试 cloudscraper 预热后携带用户Cookie再次POST")
                        headers_retry = dict(headers)
                        headers_retry.pop('Cookie', None)
                        resp_retry = warm.post(url, headers=headers_retry, timeout=30)
                        ct_retry = resp_retry.headers.get('Content-Type', '')
                        if 'application/json' in (ct_retry or '').lower():
                            data = resp_retry.json()
                            msg = data.get('message', '') if isinstance(data, dict) else ''
                            gain = self._extract_gain(data, msg)
                            if isinstance(data, dict) and data.get('success') is True:
                                result.update({"success": True, "signed": True, "message": msg})
                                if gain is not None:
                                    result["gain"] = gain
                                return result
                            elif msg and ("已完成签到" in msg or "今日已签到" in msg):
                                result.update({"success": True, "already_signed": True, "message": msg})
                                if gain is not None:
                                    result["gain"] = gain
                                return result
                except Exception as e2:
                    logger.warning(f"预热+重试失败: {str(e2)}")

                if any(k in text for k in ["鸡腿", "签到成功", "签到完成", "已完成签到"]):
                    result.update({"success": True, "signed": True, "message": text[:80]})
                elif any(k in text for k in ["今日已签到", "已签到"]):
                    result.update({"success": True, "already_signed": True, "message": text[:80]})
                elif "Cannot GET" in text and "attendance" in text:
                    result.update({"message": "服务端拒绝GET，需要POST；可能被WAF拦截"})
                elif any(k in text for k in ["登录", "注册", "陌生人"]):
                    result.update({"message": "未登录或Cookie失效，返回登录页"})
                else:
                    result.update({"message": f"非JSON响应({getattr(response, 'status_code', None)})"})

                return result

        except Exception as e:
            logger.error(f"API签到出错: {str(e)}", exc_info=True)
            return {"success": False, "message": f"API签到出错: {str(e)}"}

    @staticmethod
    def _extract_gain(data: Any, message: str = "") -> Optional[int]:
        # 从常见 JSON 字段中提取奖励值
        if isinstance(data, dict):
            for key in ["gain", "reward", "amount", "coin", "bonus"]:
                value = data.get(key)
                if isinstance(value, (int, float)) and value >= 0:
                    return int(value)
            nested = data.get("data")
            if isinstance(nested, dict):
                for key in ["gain", "reward", "amount", "coin", "bonus"]:
                    value = nested.get(key)
                    if isinstance(value, (int, float)) and value >= 0:
                        return int(value)

        # 从消息中提取“xx鸡腿”
        if message:
            matched = re.search(r"(\d+)\s*鸡腿", message)
            if matched:
                try:
                    return int(matched.group(1))
                except Exception:
                    return None
        return None

    def _build_common_headers(self, referer_path: str = "/board", with_cookie: bool = True) -> Dict[str, str]:
        origin = self._site_url
        referer_path = referer_path or "/board"
        if not referer_path.startswith("/"):
            referer_path = "/" + referer_path
        headers = {
            "Accept": "*/*",
            "Accept-Encoding": "gzip, deflate, br, zstd",
            "Accept-Language": "zh-CN,zh;q=0.9,en;q=0.8",
            "Origin": origin,
            "Referer": f"{origin}{referer_path}",
            "Sec-Fetch-Dest": "empty",
            "Sec-Fetch-Mode": "cors",
            "Sec-Fetch-Site": "same-origin",
            "User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/136.0.0.0 Safari/537.36",
        }
        if with_cookie:
            active_cookie, cookie_error = self._get_active_cookie()
            if active_cookie:
                headers["Cookie"] = active_cookie
            elif cookie_error:
                logger.warning(f"[deepfloodsign] 构建请求头时未附带 Cookie: {cookie_error}")
        return headers

    def _build_site_url(self, path: str) -> str:
        path = (path or "").strip()
        if path.startswith("http://") or path.startswith("https://"):
            return path
        if not path.startswith("/"):
            path = "/" + path
        return f"{self._site_url.rstrip('/')}{path}"

    def _fetch_user_info(self) -> Dict[str, Any]:
        active_cookie, _ = self._get_active_cookie()
        if not self._site_url or not active_cookie or not self._member_id or not self._user_info_path:
            return {}
        try:
            path = self._user_info_path
            if "{member_id}" in path:
                path = path.format(member_id=self._member_id)
            url = self._build_site_url(path)
            headers = self._build_common_headers(referer_path=f"/space/{self._member_id}", with_cookie=True)
            resp = self._smart_get(url=url, headers=headers, proxies=self._get_proxies(), timeout=30)
            data = {}
            try:
                data = resp.json()
            except Exception:
                return {}

            detail = {}
            if isinstance(data, dict):
                if isinstance(data.get("detail"), dict):
                    detail = data.get("detail")
                elif isinstance(data.get("data"), dict):
                    detail = data.get("data")
                elif isinstance(data.get("user"), dict):
                    detail = data.get("user")
                else:
                    detail = data

            if not isinstance(detail, dict):
                return {}

            normalized = {
                "member_name": detail.get("member_name") or detail.get("name") or detail.get("username") or detail.get("nick_name"),
                "rank": detail.get("rank") or detail.get("level") or detail.get("user_level"),
                "coin": detail.get("coin") or detail.get("bonus") or detail.get("chicken_leg"),
            }
            self.save_data("last_user_info", normalized)
            return normalized
        except Exception as e:
            logger.warning(f"获取用户信息失败: {str(e)}")
            return {}

    def _fetch_attendance_record(self) -> Dict[str, Any]:
        active_cookie, _ = self._get_active_cookie()
        if not self._site_url or not active_cookie or not self._attendance_record_path:
            return {}
