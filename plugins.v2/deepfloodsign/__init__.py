    def _smart_post(self, url, headers=None, data=None, json_body=None, proxies=None, timeout=30):
        # 1) cloudscraper
        if HAS_CLOUDSCRAPER and self._scraper:
            try:
                if proxies:
                    self._scraper.proxies = self._normalize_proxies(proxies) or {}
                return self._scraper.post(url, headers=headers, data=data, json=json_body, timeout=timeout) if not self._verify_ssl else self._scraper.post(url, headers=headers, data=data, json=json_body, timeout=timeout, verify=True)
            except Exception as e:
                logger.warning(f"cloudscraper 请求失败，将回退：{str(e)}")

        # 2) curl_cffi
        if HAS_CURL_CFFI:
            session = curl_requests.Session(impersonate="chrome110")
            if proxies:
                session.proxies = self._normalize_proxies(proxies) or {}
            return session.post(url, headers=headers, data=data, json=json_body, timeout=timeout) if not self._verify_ssl else session.post(url, headers=headers, data=data, json=json_body, timeout=timeout, verify=True)

        # 3) requests
        if requests is None:
            raise Exception("requests not installed")
        norm = self._normalize_proxies(proxies)
        if self._verify_ssl:
            return requests.post(url, headers=headers, data=data, json=json_body, proxies=norm, timeout=timeout, verify=True)
        return requests.post(url, headers=headers, data=data, json=json_body, proxies=norm, timeout=timeout)

