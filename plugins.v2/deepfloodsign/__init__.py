    def _run_api_sign(self) -> Dict[str, Any]:
        """尽量复用 nodeseeksign 的签到逻辑：API优先 + 非JSON兜底 + cloudscraper预热重试"""
        try:
            result = {"success": False, "signed": False, "already_signed": False, "message": "", "gain": None}

            if not self._site_url:
                result["message"] = "未配置站点 URL"
                return result
            if not self._cookie:
                result["message"] = "未配置 Cookie"
                return result

            url = self._build_attendance_url()
            origin = self._site_url
            referer = f"{self._site_url}/board"

            headers = {
                'Accept': '*/*',
                'Accept-Encoding': 'gzip, deflate, br, zstd',
                'Accept-Language': 'zh-CN,zh;q=0.9,en;q=0.8',
                'Content-Length': '0',
                'Content-Type': 'application/json',
                'Origin': origin,
                'Referer': referer,
                'Sec-Fetch-Dest': 'empty',
                'Sec-Fetch-Mode': 'cors',
                'Sec-Fetch-Site': 'same-origin',
                'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/136.0.0.0 Safari/537.36',
                'Cookie': self._cookie,
            }

            proxies = self._get_proxies()

            # 主请求
            if self._attendance_method == 'GET':
                response = self._smart_get(url=url, headers=headers, proxies=proxies, timeout=30)
            else:
                response = self._smart_post(url=url, headers=headers, data=b'', json_body=None, proxies=proxies, timeout=30)

            try:
                logger.info(f"签到响应状态码: {getattr(response, 'status_code', None)}")
                ct = response.headers.get('Content-Type') or response.headers.get('content-type')
                if ct:
                    logger.info(f"签到响应Content-Type: {ct}")
            except Exception:
                pass

            # 优先 JSON
            try:
                data = response.json()
                msg = data.get('message', '') if isinstance(data, dict) else ''

                gain = self._extract_gain(data, msg)
                if isinstance(data, dict) and data.get('success') is True:
                    result.update({"success": True, "signed": True, "message": msg})
                elif msg and ("鸡腿" in msg or ("签到" in msg and ("成功" in msg or "完成" in msg))):
                    result.update({"success": True, "signed": True, "message": msg})
                elif msg and ("已完成签到" in msg or "今日已签到" in msg):
                    result.update({"success": True, "already_signed": True, "message": msg})
                elif msg == "USER NOT FOUND" or (isinstance(data, dict) and data.get('status') == 404):
                    result.update({"message": "Cookie已失效，请更新"})
                else:
                    result.update({"message": msg or f"未知响应: {getattr(response, 'status_code', None)}"})
                if gain is not None:
                    result["gain"] = gain
                return result

            except Exception:
                # 非 JSON：记录片段 + cloudscraper 预热重试（复用 nodeseeksign 思路）
                text = getattr(response, 'text', '') or ''
                snippet = text[:400] if len(text) > 400 else text
                logger.warning(f"非JSON签到响应文本片段: {snippet}")
                try:
                    self.save_data('last_sign_response', {
                        'status_code': getattr(response, 'status_code', None),
                        'content_type': getattr(getattr(response, 'headers', {}), 'get', lambda *_: '')('Content-Type', ''),
                        'text_snippet': snippet
                    })
                except Exception:
                    pass

                try:
                    warm = self._scraper_warmup_and_attach_user_cookie()
                    if warm and self._attendance_method != 'GET':
                        logger.info("尝试 cloudscraper 预热后携带用户Cookie再次POST")
                        headers_retry = dict(headers)
                        headers_retry.pop('Cookie', None)
                        resp_retry = warm.post(url, headers=headers_retry, timeout=30)
                        ct_retry = resp_retry.headers.get('Content-Type', '')
                        if 'application/json' in (ct_retry or '').lower():
                            data = resp_retry.json()
                            msg = data.get('message', '') if isinstance(data, dict) else ''
                            gain = self._extract_gain(data, msg)
                            if isinstance(data, dict) and data.get('success') is True:
                                result.update({"success": True, "signed": True, "message": msg})
                                if gain is not None:
                                    result["gain"] = gain
                                return result
                            elif msg and ("已完成签到" in msg or "今日已签到" in msg):
                                result.update({"success": True, "already_signed": True, "message": msg})
                                if gain is not None:
                                    result["gain"] = gain
                                return result
                except Exception as e2:
                    logger.warning(f"预热+重试失败: {str(e2)}")

                if any(k in text for k in ["鸡腿", "签到成功", "签到完成", "已完成签到"]):
                    result.update({"success": True, "signed": True, "message": text[:80]})
                elif any(k in text for k in ["今日已签到", "已签到"]):
                    result.update({"success": True, "already_signed": True, "message": text[:80]})
                elif "Cannot GET" in text and "attendance" in text:
                    result.update({"message": "服务端拒绝GET，需要POST；可能被WAF拦截"})
                elif any(k in text for k in ["登录", "注册", "陌生人"]):
                    result.update({"message": "未登录或Cookie失效，返回登录页"})
                else:
                    result.update({"message": f"非JSON响应({getattr(response, 'status_code', None)})"})

                return result

        except Exception as e:
            logger.error(f"API签到出错: {str(e)}", exc_info=True)
            return {"success": False, "message": f"API签到出错: {str(e)}"}

    @staticmethod
    def _extract_gain(data: Any, message: str = "") -> Optional[int]:
        # 从常见 JSON 字段中提取奖励值
        if isinstance(data, dict):
            for key in ["gain", "reward", "amount", "coin", "bonus"]:
                value = data.get(key)
                if isinstance(value, (int, float)) and value >= 0:
                    return int(value)
            nested = data.get("data")
            if isinstance(nested, dict):
                for key in ["gain", "reward", "amount", "coin", "bonus"]:
                    value = nested.get(key)
                    if isinstance(value, (int, float)) and value >= 0:
                        return int(value)

        # 从消息中提取“xx鸡腿”
        if message:
            matched = re.search(r"(\d+)\s*鸡腿", message)
            if matched:
                try:
                    return int(matched.group(1))
                except Exception:
                    return None
        return None

    def _build_common_headers(self, referer_path: str = "/board", with_cookie: bool = True) -> Dict[str, str]:
        origin = self._site_url
        referer_path = referer_path or "/board"
        if not referer_path.startswith("/"):
            referer_path = "/" + referer_path
        headers = {
            "Accept": "*/*",
            "Accept-Encoding": "gzip, deflate, br, zstd",
            "Accept-Language": "zh-CN,zh;q=0.9,en;q=0.8",
            "Origin": origin,
            "Referer": f"{origin}{referer_path}",
            "Sec-Fetch-Dest": "empty",
            "Sec-Fetch-Mode": "cors",
            "Sec-Fetch-Site": "same-origin",
            "User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/136.0.0.0 Safari/537.36",
        }
        if with_cookie and self._cookie:
            headers["Cookie"] = self._cookie
        return headers

    def _build_site_url(self, path: str) -> str:
        path = (path or "").strip()
        if path.startswith("http://") or path.startswith("https://"):
            return path
        if not path.startswith("/"):
            path = "/" + path
        return f"{self._site_url.rstrip('/')}{path}"

    def _fetch_user_info(self) -> Dict[str, Any]:
        if not self._site_url or not self._cookie or not self._member_id or not self._user_info_path:
            return {}
        try:
            path = self._user_info_path
            if "{member_id}" in path:
                path = path.format(member_id=self._member_id)
            url = self._build_site_url(path)
            headers = self._build_common_headers(referer_path=f"/space/{self._member_id}", with_cookie=True)
            resp = self._smart_get(url=url, headers=headers, proxies=self._get_proxies(), timeout=30)
            data = {}
            try:
                data = resp.json()
            except Exception:
                return {}

            detail = {}
            if isinstance(data, dict):
                if isinstance(data.get("detail"), dict):
                    detail = data.get("detail")
                elif isinstance(data.get("data"), dict):
                    detail = data.get("data")
                elif isinstance(data.get("user"), dict):
                    detail = data.get("user")
                else:
                    detail = data

            if not isinstance(detail, dict):
                return {}

            normalized = {
                "member_name": detail.get("member_name") or detail.get("name") or detail.get("username") or detail.get("nick_name"),
                "rank": detail.get("rank") or detail.get("level") or detail.get("user_level"),
                "coin": detail.get("coin") or detail.get("bonus") or detail.get("chicken_leg"),
            }
            self.save_data("last_user_info", normalized)
            return normalized
        except Exception as e:
            logger.warning(f"获取用户信息失败: {str(e)}")
            return {}

    def _fetch_attendance_record(self) -> Dict[str, Any]:
        if not self._site_url or not self._cookie or not self._attendance_record_path:
            return {}
        try:
            url = self._build_site_url(self._attendance_record_path)
            headers = self._build_common_headers(referer_path="/board", with_cookie=True)
            resp = self._smart_get(url=url, headers=headers, proxies=self._get_proxies(), timeout=30)
            try:
                data = resp.json()
            except Exception:
                return {}

            record = {}
            if isinstance(data, dict):
                if isinstance(data.get("record"), dict):
                    record = data.get("record")
                elif isinstance(data.get("data"), dict) and isinstance(data.get("data").get("record"), dict):
                    record = data.get("data").get("record")
                elif isinstance(data.get("data"), list) and data.get("data"):
                    first = data.get("data")[0]
                    record = first if isinstance(first, dict) else {}

            if not isinstance(record, dict):
                return {}

            normalized = {
                "gain": record.get("gain") or record.get("reward") or record.get("amount"),
                "rank": record.get("rank") or data.get("order"),
                "total_signers": record.get("total_signers") or data.get("total"),
                "created_at": record.get("created_at") or record.get("create_time"),
            }
            self.save_data("last_attendance_record", normalized)
            return normalized
        except Exception as e:
            logger.warning(f"获取签到记录失败: {str(e)}")
            return {}

    def _send_sign_notification(
        self,
        sign_dict: Dict[str, Any],
        result: Dict[str, Any],
        user_info: Optional[Dict[str, Any]] = None,
        attendance_record: Optional[Dict[str, Any]] = None,
    ):
        if not self._notify:
            return

        status = sign_dict.get("status", "未知")
        sign_time = sign_dict.get("date", datetime.now().strftime("%Y-%m-%d %H:%M:%S"))
        ok = bool(result.get("success") and (result.get("signed") or result.get("already_signed")))
        title = "【✅ DeepFlood论坛签到成功】" if ok else "【❌ DeepFlood论坛签到失败】"

        user_info = user_info or {}
        attendance_record = attendance_record or {}
        gain = result.get("gain") if result.get("gain") is not None else attendance_record.get("gain")
        rank = attendance_record.get("rank")
        total_signers = attendance_record.get("total_signers")

        lines = [
            "📢 执行结果",
            "━━━━━━━━━━",
            f"🕐 时间：{sign_time}",
            f"✨ 状态：{status}",
        ]

        member_name = user_info.get("member_name")
        user_rank = user_info.get("rank")
        user_coin = user_info.get("coin")
        if member_name or user_rank or user_coin:
            lines.append(
                f"👤 用户：{member_name or '未知'}  等级：{user_rank or '未知'}  鸡腿：{user_coin if user_coin is not None else '未知'}"
            )

        if gain is not None:
            lines.append(f"🎁 获得：{gain}个鸡腿")
        if rank:
            rank_text = f"🏆 排名：第{rank}名"
            if total_signers:
                rank_text += f" (共{total_signers}人)"
            lines.append(rank_text)
        elif total_signers:
            lines.append(f"📊 今日共{total_signers}人签到")

        lines.append("━━━━━━━━━━")
        self.post_message(mtype=NotificationType.SiteMessage, title=title, text="\n".join(lines))

    def _scraper_warmup_and_attach_user_cookie(self):
        """参考 nodeseeksign：先 GET /board 预热，再把用户 Cookie 拆分塞进 cloudscraper cookie jar。"""
        try:
            if not (HAS_CLOUDSCRAPER and self._scraper):
                return None

            proxies = self._get_proxies()
            if proxies:
                self._scraper.proxies = self._normalize_proxies(proxies) or {}

            self._scraper.get(f"{self._site_url}/board", timeout=30)

            # 尝试推断 domain
            domain = None
            try:
                from urllib.parse import urlparse
                domain = urlparse(self._site_url).hostname
            except Exception:
                domain = None

            base = self._cookie or ''
            for part in base.split(';'):
                kv = part.strip().split('=', 1)
                if len(kv) == 2:
                    name, value = kv[0].strip(), kv[1].strip()
                    if name and value:
                        if domain:
                            self._scraper.cookies.set(name, value, domain=domain)
                        else:
                            self._scraper.cookies.set(name, value)
            return self._scraper
        except Exception as e:
            logger.warning(f"cloudscraper 预热失败: {str(e)}")
            return None
    def sign(self):
        logger.info("============= 开始 DeepFlood 签到 =============")

        if not self._cookie:
            sign_dict = {"date": datetime.now().strftime('%Y-%m-%d %H:%M:%S'), "status": "签到失败: 未配置Cookie"}
            self._save_sign_history(sign_dict)
            if self._notify:
                self.post_message(mtype=NotificationType.SiteMessage, title="【DeepFlood论坛签到失败】", text="未配置Cookie，请在设置中添加Cookie")
            return sign_dict

        if not self._site_url:
            sign_dict = {"date": datetime.now().strftime('%Y-%m-%d %H:%M:%S'), "status": "签到失败: 未配置站点URL"}
            self._save_sign_history(sign_dict)
            if self._notify:
                self.post_message(mtype=NotificationType.SiteMessage, title="【DeepFlood论坛签到失败】", text="未配置站点URL，请在设置中填写")
            return sign_dict

        self._wait_random_interval()

        last_exc = None
        for attempt in range(1, int(self._max_retries) + 1):
            try:
                result = self._run_api_sign()
                break
            except Exception as e:
                last_exc = e
                logger.warning(f"签到请求异常，准备重试({attempt}/{self._max_retries}): {str(e)}")
                time.sleep(2)
        else:
            result = {"success": False, "message": f"重试后仍失败: {last_exc}"}

        user_info = {}
        attendance_record = {}
        try:
            if self._member_id:
                user_info = self._fetch_user_info()
        except Exception:
            user_info = {}
        try:
            attendance_record = self._fetch_attendance_record()
            if attendance_record.get("gain") is not None and result.get("gain") is None:
                result["gain"] = attendance_record.get("gain")
        except Exception:
            attendance_record = {}

        if result.get('success') and result.get('signed'):
            status = f"签到成功: {result.get('message','')}"
        elif result.get('success') and result.get('already_signed'):
            status = f"今日已签到: {result.get('message','')}"
        else:
            status = f"签到失败: {result.get('message','未知错误')}"

        sign_dict = {"date": datetime.now().strftime('%Y-%m-%d %H:%M:%S'), "status": status}
        if result.get("gain") is not None:
            sign_dict["gain"] = result.get("gain")
        if attendance_record.get("rank") is not None:
            sign_dict["rank"] = attendance_record.get("rank")
        if attendance_record.get("total_signers") is not None:
            sign_dict["total_signers"] = attendance_record.get("total_signers")
        self._save_sign_history(sign_dict)

        if self._notify:
            self._send_sign_notification(
                sign_dict=sign_dict,
                result=result,
                user_info=user_info,
                attendance_record=attendance_record,
            )

        return sign_dict

    def _save_sign_history(self, sign_data):
        try:
            history = self.get_data('sign_history') or []
            if "date" not in sign_data:
                sign_data["date"] = datetime.now().strftime('%Y-%m-%d %H:%M:%S')
            history.append(sign_data)

            retention_days = int(self._history_days) if self._history_days is not None else 30
            now = datetime.now()
            valid_history = []
            for record in history:
                try:
                    record_date = datetime.strptime(record["date"], '%Y-%m-%d %H:%M:%S')
                    if (now - record_date).days < retention_days:
                        valid_history.append(record)
                except Exception:
                    record["date"] = datetime.now().strftime('%Y-%m-%d %H:%M:%S')
                    valid_history.append(record)

            self.save_data(key="sign_history", value=valid_history)
        except Exception as e:
            logger.error(f"保存签到历史记录失败: {str(e)}", exc_info=True)

    def stop_service(self):
        try:
            if self._scheduler:
                self._scheduler.remove_all_jobs()
                if self._scheduler.running:
                    self._scheduler.shutdown()
                self._scheduler = None
        except Exception:
            pass

