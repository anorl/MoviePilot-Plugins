    def _normalize_proxies(self, proxies_input):
        try:
            if not proxies_input:
                return None
            if isinstance(proxies_input, str):
                return {"http": proxies_input, "https": proxies_input}
            if isinstance(proxies_input, dict):
                http_url = proxies_input.get("http") or proxies_input.get("HTTP") or proxies_input.get("https") or proxies_input.get("HTTPS")
                https_url = proxies_input.get("https") or proxies_input.get("HTTPS") or proxies_input.get("http") or proxies_input.get("HTTP")
                if not http_url and not https_url:
                    return None
                return {"http": http_url or https_url, "https": https_url or http_url}
        except Exception as e:
            logger.warning(f"代理归一化失败，将忽略代理: {str(e)}")
        return None

    def _get_proxies(self):
        if not self._use_proxy:
            return None
        try:
            if hasattr(settings, 'PROXY') and settings.PROXY:
                return self._normalize_proxies(settings.PROXY)
        except Exception:
            pass
        return None

    def _wait_random_interval(self):
        try:
            min_delay = float(self._min_delay) if self._min_delay is not None else 5.0
            max_delay = float(self._max_delay) if self._max_delay is not None else 12.0
            if max_delay >= min_delay and min_delay > 0:
                delay = random.uniform(min_delay, max_delay)
                logger.info(f"请求前随机等待 {delay:.2f} 秒...")
                time.sleep(delay)
        except Exception:
            pass

    def _smart_post(self, url, headers=None, data=None, json_body=None, proxies=None, timeout=30):
        # 1) cloudscraper
        if HAS_CLOUDSCRAPER and self._scraper:
            try:
                if proxies:
                    self._scraper.proxies = self._normalize_proxies(proxies) or {}
                return self._scraper.post(url, headers=headers, data=data, json=json_body, timeout=timeout) if not self._verify_ssl else self._scraper.post(url, headers=headers, data=data, json=json_body, timeout=timeout, verify=True)
            except Exception as e:
                logger.warning(f"cloudscraper 请求失败，将回退：{str(e)}")

        # 2) curl_cffi
        if HAS_CURL_CFFI:
            session = curl_requests.Session(impersonate="chrome110")
            if proxies:
                session.proxies = self._normalize_proxies(proxies) or {}
            return session.post(url, headers=headers, data=data, json=json_body, timeout=timeout) if not self._verify_ssl else session.post(url, headers=headers, data=data, json=json_body, timeout=timeout, verify=True)

        # 3) requests
        if requests is None:
            raise Exception("requests not installed")
        norm = self._normalize_proxies(proxies)
        if self._verify_ssl:
            return requests.post(url, headers=headers, data=data, json=json_body, proxies=norm, timeout=timeout, verify=True)
        return requests.post(url, headers=headers, data=data, json=json_body, proxies=norm, timeout=timeout)

